let prefLang = prompt('Which language do you prefer? Type "es" for español, "de" for deutsch, "en" for english, and "fr" for française')

if (prefLang = "es") {
    document.write('Hola Mundo')
} else if (prefLang = "de") {
    document.write('Hallo Welt')
} else if (prefLang = "en") {
    document.write('Hello World')
} else if (prefLang = "fr") {
    document.write('Bonjour le Monde')
}
else {
    document.write('Hello World')
}